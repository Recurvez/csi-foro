    <h1>Política de Privacidad</h1>
    <p>Última actualización: 23/02/2025</p>
    <p>En ForoCSI, nos comprometemos a proteger tu privacidad y garantizar que tus datos personales estén seguros. Esta Política de Privacidad explica qué información recopilamos, cómo la usamos y los derechos que tienes sobre tus datos.</p>
    
    <h2>1. Información que recopilamos</h2>
    <p>Cuando te registras y usas nuestro foro, recopilamos la siguiente información:</p>
    <ul>
        <li><strong>Nombre:</strong> para identificarte dentro de la comunidad.</li>
        <li><strong>Correo electrónico:</strong> para gestionar tu cuenta y enviarte notificaciones relacionadas con el foro.</li>
        <li><strong>Dirección IP:</strong> para garantizar la seguridad del foro, prevenir fraudes y administrar el sitio.</li>
        <li><strong>Cookies:</strong> recopilamos datos a través de cookies para mejorar la experiencia del usuario (puedes consultar más información en nuestra Política de Cookies).</li>
    </ul>
    
    <h2>2. ¿Para qué usamos tus datos?</h2>
    <p>Usamos tus datos personales con las siguientes finalidades:</p>
    <ul>
        <li><strong>Gestión de cuentas:</strong> para permitirte registrarte, iniciar sesión y participar en el foro.</li>
        <li><strong>Seguridad:</strong> para prevenir actividades fraudulentas y proteger la integridad de la plataforma.</li>
        <li><strong>Comunicaciones:</strong> para enviarte actualizaciones, notificaciones o responder a tus consultas.</li>
        <li><strong>Mejora del foro:</strong> para analizar el uso del sitio y mejorar su funcionamiento.</li>
    </ul>
    
    <h2>3. Base legal para el tratamiento de datos</h2>
    <p>Nos basamos en las siguientes bases legales para procesar tus datos:</p>
    <ul>
        <li><strong>Consentimiento:</strong> cuando te registras y aceptas nuestra política de privacidad.</li>
        <li><strong>Interés legítimo:</strong> para garantizar la seguridad del sitio y prevenir fraudes.</li>
    </ul>
    
    <h2>4. Compartición de datos</h2>
    <p>No compartimos tus datos personales con terceros, salvo que sea necesario para cumplir con obligaciones legales o proteger nuestros derechos.</p>
    
    <h2>5. Almacenamiento de datos</h2>
    <p>Tus datos serán almacenados de forma segura mientras mantengas tu cuenta activa. Si decides eliminar tu cuenta, borraremos tus datos salvo aquellos que debamos conservar por obligaciones legales.</p>
    
    <h2>6. Tus derechos</h2>
    <p>De acuerdo con las leyes de protección de datos aplicables, tienes derecho a:</p>
    <ul>
        <li>Acceder a los datos personales que tenemos sobre ti.</li>
        <li>Rectificar datos inexactos o incompletos.</li>
        <li>Eliminar tus datos personales cuando ya no sean necesarios.</li>
        <li>Oponerte al tratamiento de tus datos para ciertos fines.</li>
        <li>Portabilidad de datos, cuando proceda.</li>
    </ul>
