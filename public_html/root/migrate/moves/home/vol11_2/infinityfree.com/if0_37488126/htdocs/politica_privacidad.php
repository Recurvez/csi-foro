    <h2>7. Seguridad</h2>
    <p>Hemos implementado medidas técnicas y organizativas para proteger tus datos contra accesos no autorizados, alteración o destrucción.</p>
    
    <h2>8. Cambios en esta política</h2>
    <p>Nos reservamos el derecho de modificar esta política. Te notificaremos sobre cualquier cambio importante.</p>
